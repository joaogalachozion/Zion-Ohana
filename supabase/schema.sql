-- Tabela de igrejas
CREATE TABLE IF NOT EXISTS igrejas (
  id TEXT PRIMARY KEY,
  nome TEXT NOT NULL,
  cidade TEXT DEFAULT '',
  estado TEXT DEFAULT '',
  pais TEXT DEFAULT 'Brasil',
  pastor TEXT DEFAULT '',
  tel_pastor TEXT DEFAULT '',
  resp_fin TEXT DEFAULT '',
  tel_fin TEXT DEFAULT '',
  status TEXT DEFAULT '⚠️ Revisão',
  link_contrato TEXT DEFAULT '',
  created_at TIMESTAMPTZ DEFAULT NOW()
);

-- Tabela de lançamentos
CREATE TABLE IF NOT EXISTS lancamentos (
  id BIGSERIAL PRIMARY KEY,
  mes TEXT NOT NULL,
  ano INTEGER NOT NULL,
  periodo TEXT NOT NULL,
  periodo_num INTEGER NOT NULL,
  igreja_id TEXT REFERENCES igrejas(id),
  dps DECIMAL(12,2) DEFAULT 0,
  "of" DECIMAL(12,2) DEFAULT 0,
  status TEXT DEFAULT '✅ Pago',
  created_at TIMESTAMPTZ DEFAULT NOW()
);

-- Habilitar RLS (Row Level Security) com acesso público para leitura/escrita
ALTER TABLE igrejas ENABLE ROW LEVEL SECURITY;
ALTER TABLE lancamentos ENABLE ROW LEVEL SECURITY;

CREATE POLICY "allow_all_igrejas" ON igrejas FOR ALL USING (true) WITH CHECK (true);
CREATE POLICY "allow_all_lancamentos" ON lancamentos FOR ALL USING (true) WITH CHECK (true);

